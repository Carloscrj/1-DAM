package dam.pruebas.operadores;

public class PruebasOpentern {

	public static void main(String[] args) {
		
	//pruebas operador con el condicional ternario
		
		int m = 7;
	    int n = 5;
	    System.out.println("m =" +m+ "y n= " +n);
	    
	    int mayor = (m>n)? m : n; //si m>n guarda m y si no, guarda n
	    System.out.println(" mayor=" + mayor);

	}

}
