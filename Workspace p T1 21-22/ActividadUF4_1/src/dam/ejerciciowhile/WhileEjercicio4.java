package dam.ejerciciowhile;



public class WhileEjercicio4 {

	public static void main(String[] args) {
		
		 
		int numero = 0;
		int suma = 0;
		
		while(numero<=100) {
			
			suma= suma+numero;
			numero= numero+1;
		}
			
		System.out.println("El sumatorio de los números enteros del 1 al 100 es "+suma);	
				
			
			
	}

}
